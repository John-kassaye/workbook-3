public class Product2 {
    private String name;
    private String id;
    private String price;

    public Product2(String name, String id, String price) {
        this.name = name;
        this.id = id;
        this.price = price;
    }

    public String getName() {
        return this.name;
    }

    public String getId() {
        return this.id;
    }

    public String getPrice() {
        return this.price;
    }

}
